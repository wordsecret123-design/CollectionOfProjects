    public boolean CheckIfAccountExistsInDB(){
        
        Statement st;
        ResultSet rs;
        if(!SI_UserIDJtext.getText().equals(""))
            userid = Integer.parseInt(SI_UserIDJtext.getText().replaceAll(" ",""));
        
        try {
            st = getConnection().createStatement();
            rs = st.executeQuery("SELECT* FROM employee");
            
            while(rs.next()){
                
                char[] password = rs.getString("emp_password").toCharArray();
                
                if(SI_UsernameJtext.getText().equals(rs.getString("emp_username"))&&
                        Arrays.equals(SI_PasswordJpassword.getPassword(), password)&&
                        SI_TypeComboBox.getSelectedItem().equals(rs.getString("emp_userType"))&&
                        userid == rs.getInt("emp_id")){
                    return true;
                }
                
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(SignIn.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return false;
    }

