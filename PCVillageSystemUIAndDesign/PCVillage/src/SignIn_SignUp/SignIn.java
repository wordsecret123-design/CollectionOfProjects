    private void SI_signInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SI_signInButtonActionPerformed
        
        if(CheckIfAccountExistsInDB()==false){
            if(SI_TypeComboBox.getSelectedItem().equals("<select user>")){
                JOptionPane.showMessageDialog(this,"Please select a user type"); 
            }
            else if(SI_PasswordJpassword.getPassword().length==0||
                    SI_UsernameJtext.getText().equals("")||
                    SI_UserIDJtext.getText().equals("")){
                
            JOptionPane.showMessageDialog(this,"One or more fields is empty. Try again.");        
            }
            else{
                JOptionPane.showMessageDialog(this,"Account does not exist. Try again.");
            }
        }
        
        else {
            
            new AdminUi(new GetUser((String)SI_TypeComboBox.getSelectedItem(),SI_UsernameJtext.getText(),userid)).setVisible(true);
            this.dispose();
        } 
        
    }//GEN-LAST:event_SI_signInButtonActionPerformed

